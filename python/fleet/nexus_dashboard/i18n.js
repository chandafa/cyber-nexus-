let LANG = localStorage.getItem("nx_lang") || "id";

function t(key, vars) {
  let s = (I18N[LANG] && I18N[LANG][key]) || (I18N.en[key]) || key;
  if (vars) for (const k in vars) s = s.replace(`{${k}}`, vars[k]);
  return s;
}

function setLang(lang) {
  LANG = I18N[lang] ? lang : "en";
  localStorage.setItem("nx_lang", LANG);
  applyI18n();
}

/** Set teks/placeholder/title untuk elemen ber-atribut data-i18n*. */
function applyI18n(root = document) {
  root.querySelectorAll("[data-i18n]").forEach((el) => (el.textContent = t(el.getAttribute("data-i18n"))));
  root.querySelectorAll("[data-i18n-ph]").forEach((el) => (el.placeholder = t(el.getAttribute("data-i18n-ph"))));
  root.querySelectorAll("[data-i18n-title]").forEach((el) => (el.title = t(el.getAttribute("data-i18n-title"))));
  document.documentElement.lang = LANG;
  if (window.onLangChange) window.onLangChange();
}
